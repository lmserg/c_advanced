#include <stdio.h>

int main() {
    int N;
    scanf("%d", &N); // Считываем сдвиг N

    char c;
    while ((c = getchar()) != '.') { // Читаем символы до точки
        if (c >= 'a' && c <= 'z') {
            // Шифруем строчные буквы
            c = (c - 'a' + N) % 26 + 'a';
        } else if (c >= 'A' && c <= 'Z') {
            // Шифруем заглавные буквы
            c = (c - 'A' + N) % 26 + 'A';
        }
        // Пробелы и другие символы выводятся без изменений
        putchar(c);
    }

    putchar('.'); // Выводим завершающую точку
    //putchar('\n'); // Переход на новую строку

    return 0;
}